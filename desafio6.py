# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadarada
n = int(input('Digite um número: '))
print('Número', n, '\n O dobro é:', n*2, '\n O triplo é:', n*3, '\n A raiz quadrada é:', n**2)

print('Número {}, o dobro {}, o triplo {} e a raiz quadrada {}'.format(n, n*2, n*3, n**(1/2)))
